PeerJ.Tools.ToastNotifications = {

    defaultTimeout: 3500,

    //data = { type, title, text, timeout(optional) }
    add: function(data){
        var toastId = Date.now();

        function getCssClass(){
            var allowedTypes = ['success','error','info','warning'];
            if(data.type && (allowedTypes.indexOf(data.type) >= 0)) {
                return 'alert alert-' + data.type.toLowerCase();
            }
            return 'alert alert-info';
        };

        function getTitle(){
            return data.title ? '<strong>' + data.title + '</strong><br>' : '';
        }

        function getText(){
            return data.text;
        }

        var toast = '<div class="js-toast-notification pj-toast-notification pj-toast-notification-hidden ' + getCssClass() + '" data-id="' + toastId + '">' + getTitle() + getText() + '</div>';
        var timeout = data.timeout ? data.timeout : this.defaultTimeout;

        $('#toast-container').append(toast);

        setTimeout(function(){
            $('.js-toast-notification[data-id="' + toastId + '"]').removeClass('pj-toast-notification-hidden');
        }, 1);

        setTimeout(function(){
            this.remove(toastId);
        }.bind(this), timeout);
    },

    remove: function(id){
        var el = $('.js-toast-notification[data-id="' + id + '"]');
        el.addClass('pj-toast-notification-hidden');
        setTimeout(function(){
            el.remove();
        }, 1000);
    }

};
PeerJ.Tools.Route = {

    getQueryParam: function(name) {
        name = name.replace(/[\[]/, '\\[').replace(/[\]]/, '\\]');
        var regex = new RegExp('[\\?&]' + name + '=([^&#]*)');
        var results = regex.exec(location.search);
        return results === null ? '' : decodeURIComponent(results[1].replace(/\+/g, ' '));
    }

};
