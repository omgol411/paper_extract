PeerJ.Article.Moment = new function() {
    this.init = function() {
        showRelativeTime();
        setInterval(showRelativeTime, 5000);
    };

    var showRelativeTime = function() {
        $("time.relative").each(function() {
            var node = $(this);

            var d = node.attr("datetime") || node.text();

            var date = new Date(d);
            if (date) {
                node.text(moment(date).fromNow());
            }
        });

        var now = moment();

        // try to match TimeDifferenceExtension::shortTimeFromNow

        $("time.relative-short").each(function () {
            var node = $(this);

            var d = node.attr("datetime") || node.text();

            if (d) {
                var diff;
                var m = moment(d);
                var days = now.diff(m, 'days');

                if (days === 0) {
                    var hours = now.diff(m, 'hours');

                    diff = Math.abs(hours) + 'h';
                } else {
                    diff = Math.abs(days) + 'd';
                }

                node.text(diff);
            }
        });
    };
    this.showRelativeTime = showRelativeTime;
};

